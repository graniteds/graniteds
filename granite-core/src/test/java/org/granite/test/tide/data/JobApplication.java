package org.granite.test.tide.data;

import java.sql.Date;

import javax.persistence.CascadeType;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;

@Entity
public class JobApplication extends AbstractEntity {
    
    private static final long serialVersionUID = 1L;

	@ManyToOne(fetch=FetchType.EAGER, cascade=CascadeType.ALL)//, cascade = CascadeType.REFRESH)
	@JoinColumn(name="user_id", nullable=false, updatable=true, insertable=true)
	private User user;

	@ManyToOne(fetch=FetchType.EAGER, cascade=CascadeType.ALL)//, cascade = CascadeType.ALL)
	@JoinColumn(name="job_id", nullable=false, updatable=true, insertable=true)
	private Job job;

	@Column(name="date")
	private Date date;

	public User getUser() {
		return user;
	}

	public void setUser(User user) {
		this.user = user;
	}

	public Job getJob() {
		return job;
	}

	public void setJob(Job job) {
		this.job = job;
	}

	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}
	
}
